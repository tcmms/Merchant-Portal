export function getStatusCounts(catalog: Category[]) {
  const counts: Record<string, number> = {
    all: 0,
    active: 0,
    inactive: 0,
    rejected: 0,
    archived: 0,
    awaiting_approval: 0,
    draft: 0,
  }
  catalog.forEach(cat =>
    cat.subCategories.forEach(sub =>
      sub.items.forEach(item => {
        counts.all++
        counts[item.status]++
      }),
    ),
  )
  return counts
}
